export default function LiquidGlassNav() {
  const [hoveredIndex, setHoveredIndex] = useState<number | null>(null);

  return (
    <nav
      className="relative flex w-fit items-center justify-center gap-1 rounded-full px-3 py-2.5"
      style={{
        background: "rgba(18, 18, 18, 0.65)",
        backdropFilter: "blur(24px) saturate(1.2)",
        WebkitBackdropFilter: "blur(24px) saturate(1.2)",
        boxShadow:
          "inset 1px 1px 2px rgba(255,255,255,0.08), inset -1px -1px 2px rgba(0,0,0,0.2), 0 8px 32px rgba(0,0,0,0.2)",
        border: "1px solid rgba(255,255,255,0.06)",
      }}
    >
      {/* Hover glow - yellowish-orange elongated oval centered on hovered icon */}
      {hoveredIndex !== null && (
        <span
          className="pointer-events-none absolute top-1/2 h-8 w-14 -translate-x-1/2 -translate-y-1/2 rounded-full transition-all duration-300 ease-out"
          style={{
            left: `${((hoveredIndex + 0.5) / NAV_ITEMS.length) * 100}%`,
            background:
              "radial-gradient(ellipse 100% 80% at 50% 50%, rgba(251, 146, 60, 0.45) 0%, rgba(234, 88, 12, 0.2) 50%, transparent 75%)",
            filter: "blur(10px)",
          }}
        />
      )}
      {NAV_ITEMS.map((item, index) => (
        <a
          key={item.id}
          href={item.href}
          className="relative z-10 flex items-center justify-center rounded-full p-2 text-white/95 transition-colors duration-200 hover:text-white"
          onMouseEnter={() => setHoveredIndex(index)}
          onMouseLeave={() => setHoveredIndex(null)}
        >
          {item.icon}
        </a>
      ))}
    </nav>
  );
}
